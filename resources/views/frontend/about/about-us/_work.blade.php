                    <h1 class="heading">OUR WORK PROCESS</h1>
                    <p class="sub-heading">Before installing the solar energy panels, we set an appointment to visit the site, check your electrical components, and take pictures of the site. The appropriate location is usually the roof, however, if the installation on it is not applicable, the panels can be installed on the ground, but making sure that there are no Anything that prevents the sun from reaching it.The following steps explain solar panel installation on a roof:</p><br/>
                    <p> 1. Set Up Scaffolding Firstly, you have to erect scaffolding to ensure safety during the whole installation process when being on the roof.</p>

                    <p> 2. Install Solar Panel Mounts
                        Then, the solar panel mounting system has to be set up. This will support the base of the solar panels. The whole mounting structure must be tilted and have an angle between 18 to 36 degrees to have maximum sunlight exposure.</p>

                    <p> 3. Install the Solar Panels When the mounts are set up, the solar panel itself has to be installed on the mounting structure. Make sure to tighten up all the bolts and nuts so that it stays stable.</p>

                    <p> 4. Wire the Solar Panels The next step in the installation process is to install the electrical wiring. In most cases, MC4 connectors are used because they are suited for all types of solar panels. Make sure to shut off the household’s electricity supply during the wiring installation.</p>

                    <p> 5. Install Solar Inverter After that, the solar inverter must be connected to the system. It is typically installed near the main panel and it could be both indoors and outdoors. Inverters are more efficient if kept in a cooler place.</p>

                    <p> 6. Bond Solar Inverter and Solar Battery Thereafter, the solar inverter has to be connected to the solar battery. The solar battery storage can save you from worrying about the lack of usable energy during cloudy times, it can also lower the solar battery storage system costs during installation.</p>

                    <p> 7. Connect the Inverter to the Consumer Unit The inverter should be connected to the consumer unit to generate electricity. A generation meter should also be connected to monitor the amount of electricity the solar panels actually produce. You can use your computer or other device to check your solar system’s performance.</p>

                    <p> 8. Start and Test Solar Panels The final step is to switch the power on and test the newly installed solar panel system. After that, the solar panel installation process is completed.</p>
